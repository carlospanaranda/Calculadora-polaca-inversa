    private void convertirDatos() {
        String valorInfija = this.txtInfija.getText();

        this.expresionInfija = new Infija(valorInfija);

        if (this.expresionInfija.getError() != null) {
            JOptionPane.showMessageDialog(this, this.expresionInfija.getError());
        } else {
            this.btnLimpiar.setEnabled(true);
            this.btnResolver.setEnabled(true);

            this.txtPostfija.setText(this.expresionInfija.getPostfijo());
        }
    }


